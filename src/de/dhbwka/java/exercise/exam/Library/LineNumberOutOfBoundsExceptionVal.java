package de.dhbwka.java.exercise.exam.Library;


public class LineNumberOutOfBoundsExceptionVal extends Exception {
    public LineNumberOutOfBoundsExceptionVal() {
    }

    public LineNumberOutOfBoundsExceptionVal(String message) {
        super(message);
    }

    public LineNumberOutOfBoundsExceptionVal(String message, Throwable cause) {
        super(message, cause);
    }

    public LineNumberOutOfBoundsExceptionVal(Throwable cause) {
        super(cause);
    }

    public LineNumberOutOfBoundsExceptionVal(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
